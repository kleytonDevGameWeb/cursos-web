// operador de adição(+) soma números:
let x = 5 + 5;

// operador de subtração(-) subtrai números.
let x1 = 5 - 5;

// operador de multiplicação(*) multiplica números.
let x2 = 5 * 5;

// operador de divisão(/) divide números.
let x3 = 5 / 5;

// operador de módulo(%) retorna o resto da divisão.
let x4 = 5 % 5;

// operador de incremento(++) incrementa números.
let x5 = 5;
x5++;

// operador de decremento(--) decrementa números.
let x6 = 5;
x6--;

// operador de exponenciação(**) eleva o primeiro operando à potência do segundo operando.
let x7 = 6 ** 2;

